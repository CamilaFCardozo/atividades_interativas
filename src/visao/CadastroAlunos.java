    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (flag == 1) {
            try {
                SalvandoFoto();
                mod.setImagem(byteArray);
                mod.setNome(txtNome.getText());
                mod.setSerie(txtSerie.getText());
                mod.setAno_cadastro((String)ComboBoxAno.getSelectedItem());

                //Salvando os dados inseridos
                dao.Salvar(mod);

                //Limpando Campos após salvar
                txtCod_acesso.setText("");
                lblImagem.setIcon(null);
                lblImagem.setText("Inserir Foto");
                txtNome.setText("");
                txtSerie.setText("");
            } catch (IOException ex) {
                Logger.getLogger(CadastroAlunos.class.getName()).log(Level.SEVERE, null, ex);
            }

            //Bloqueando os campos novamente
            lblImagem.setEnabled(false);
            txtNome.setEnabled(false);
            txtSerie.setEnabled(false);
            ComboBoxAno.setEnabled(false);
            btnNovo.setEnabled(true);
            btnSalvar.setEnabled(false);
            btnCancelar.setEnabled(false);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
        } else {

            try {
                //Dados a ser alterados
                
                mod.setCod_acesso(Integer.parseInt(txtCod_acesso.getText()));
                SalvandoFoto();
                mod.setImagem(byteArray);
                mod.setNome(txtNome.getText());
                mod.setSerie(txtSerie.getText());
                mod.setAno_cadastro((String)ComboBoxAno.getSelectedItem());

                // Editar
                dao.EditarUser(mod);

                //Limpando Campos após salvar
                txtCod_acesso.setText("");
                lblImagem.setIcon(null);
                lblImagem.setText("Inserir a Foto");
                txtNome.setText("");
                txtSerie.setText("");
                txtPesquisa.setText("");

                //Bloqueando os campos novamente
                lblImagem.setEnabled(false);
                txtNome.setEnabled(false);
                txtSerie.setEnabled(false);
                ComboBoxAno.setEnabled(false);
                btnNovo.setEnabled(true);
                btnSalvar.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnEditar.setEnabled(false);
                btnExcluir.setEnabled(false);
            } catch (IOException ex) {
                Logger.getLogger(CadastroAlunos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

