    public void getConsultar() throws IOException, SQLException, ClassNotFoundException {
        imagemBuffer = null;
        if (mod != null) {
            imagemBuffer = ImageIO.read(new ByteArrayInputStream(mod.getImagem()));
            Image diminuirImage = imagemBuffer.getScaledInstance(220, 100, 0);
            lblImagem.setText("");
            lblImagem.setIcon(new ImageIcon(diminuirImage));

        } else {
            JOptionPane.showMessageDialog(this, "Não existe cadastro");
        }

    }

    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        mod.setPesquisa(txtPesquisa.getText());
        BeansAlunos model = dao.buscaAlunos(mod);
        try {

            txtCod_acesso.setText(String.valueOf(model.getCod_acesso()));// valueOf transforma valor em string
            getConsultar();
            txtNome.setText(model.getNome());
            txtSerie.setText(model.getSerie());
            ComboBoxAno.setSelectedItem(model.getAno_cadastro());
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnNovo.setEnabled(false);
            btnCancelar.setEnabled(true);
            btnSalvar.setEnabled(false);

        } catch (IOException ex) {
            Logger.getLogger(CadastroAlunos.class.getName()).log(Level.SEVERE, null, ex);
            lblImagem.setIcon(null);
            lblImagem.setText("Inserir a Foto");
        } catch (SQLException ex) {
            Logger.getLogger(CadastroAlunos.class.getName()).log(Level.SEVERE, null, ex);
            lblImagem.setIcon(null);
            lblImagem.setText("Inserir a Foto");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CadastroAlunos.class.getName()).log(Level.SEVERE, null, ex);
            lblImagem.setIcon(null);
            lblImagem.setText("Inserir a Foto");
        }

    }//GEN-LAST:event_btnPesquisaActionPerformed

